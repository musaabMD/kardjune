const distractors = [
  "Choose the longest option because exam writers often include more detail there.",
  "Rely on a memorized phrase without checking whether it fits the scenario.",
  "Skip the data in the stem and answer from personal experience.",
  "Focus on a low-risk detail before addressing the main tested objective.",
  "Select an option that sounds familiar even though it does not solve the problem.",
  "Delay the decision until every possible fact has been reviewed.",
  "Treat all choices as equally likely and guess before identifying the domain.",
  "Ignore qualifying words such as first, best, except, most, and priority.",
];

const templates = [
  (exam, subject) => ({
    prompt: `A learner is starting ${exam.title} review for ${subject}. Which study action best matches the way this topic is usually tested?`,
    answer: `Build a checklist for ${subject.toLowerCase()} that links the stem clues to the safest or most defensible next step.`,
    explanation: `${subject} questions reward recognizing the tested objective first, then choosing the action that directly addresses it.`,
    tag: "Study strategy",
  }),
  (exam, subject) => ({
    prompt: `On a ${exam.title} practice set, a question about ${subject} includes several attractive distractors. What should the candidate do first?`,
    answer: `Identify the command word and the highest-value clue before comparing the answer choices.`,
    explanation: `Command words and key clues narrow the task, which prevents choosing a familiar but off-target answer in ${subject}.`,
    tag: "Question analysis",
  }),
  (exam, subject) => ({
    prompt: `Which answer choice would be most consistent with strong ${exam.title} performance in ${subject}?`,
    answer: `An option that applies the relevant rule, standard, or clinical principle to the facts given in the stem.`,
    explanation: `Most certification items test application. The best choice uses the topic rule in context rather than repeating a vague definition.`,
    tag: "Application",
  }),
  (exam, subject) => ({
    prompt: `A candidate repeatedly misses ${subject} items while preparing for ${exam.title}. What remediation step is most useful?`,
    answer: `Review missed-question rationales and group them by the underlying concept being tested.`,
    explanation: `Grouping misses by concept turns scattered errors into a targeted review plan for ${subject}.`,
    tag: "Remediation",
  }),
  (exam, subject) => ({
    prompt: `During timed ${exam.title} practice, what is the best approach to a difficult ${subject} item?`,
    answer: `Eliminate choices that conflict with the stem, choose the most defensible remaining option, and mark it for review if time allows.`,
    explanation: `A disciplined elimination process protects time while still using the evidence available in the question.`,
    tag: "Timed practice",
  }),
];

function pickDistractors(seed, count = 3) {
  const rotated = distractors.slice(seed % distractors.length).concat(distractors.slice(0, seed % distractors.length));
  return rotated.slice(0, count);
}

function buildQuestion(exam, subject, index) {
  const template = templates[index % templates.length];
  const item = template(exam, subject, index + 1);
  const answerIndex = index % 4;
  const options = pickDistractors(index).slice();
  options.splice(answerIndex, 0, item.answer);

  return {
    _id: `${exam.slug}-${String(index + 1).padStart(3, "0")}`,
    examId: exam.id,
    subject,
    prompt: item.prompt,
    options,
    answerIndex,
    explanation: item.explanation,
    tags: ["Starter", item.tag],
  };
}

for (const exam of exams) {
  const questions = Array.from({ length: 50 }, (_, index) => {
    const subject = exam.subjects[index % exam.subjects.length];
    return buildQuestion(exam, subject, index);
  });

  const dir = join(outRoot, exam.slug);
  mkdirSync(dir, { recursive: true });
  writeFileSync(join(dir, "all.json"), `${JSON.stringify({ questions }, null, 2)}\n`);
}

console.log(`Generated ${exams.length} qbanks with ${exams.length * 50} total questions.`);
